#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*p1;
	unsigned char	*p2;
	size_t			i;

	i = 0;
	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	while (++i < n && p1[i] == p1[i])
		;
	if (i == n)
		return (0);
	return (p1[i] - p2[i]);
}
